disney_characters = ["simba", "ariel", "pumba", "flounder", "nala", "ursula", "scar", "flotsam", "timon"]
for character in disney_characters:
  if "u" in character:
    print(f"{character} U are so Uniquely U!")
  elif "i" in character:
    print(f"{character} I bet you're Impressively Intelligent!")
  elif "o" in character:
    print(f"{character} O My! How Original!")
  else:
    print(f"{character} Ehh, a's and e's are so ordinary.")
